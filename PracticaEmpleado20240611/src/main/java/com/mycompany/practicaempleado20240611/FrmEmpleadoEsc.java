    private void crearEmpleado() {
        try {
            Empleado empleado = obtenerDatos();
            EmpleadoDAL.insertar(empleado);
            JOptionPane.showMessageDialog(this,
                    "El empleado fue registrado exitosamente", "CREAR EMPLEADO",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private void modificarEmpleado() {
        try {
            Empleado empleado = obtenerDatos();
            EmpleadoDAL.actualizar(empleado);
            JOptionPane.showMessageDialog(this,
                    "El empleado fue modificado exitosamente", "MODIFICAR EMPLEADO",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), "ERROR EMPLEADO",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private void eliminarEmpleado() {
        try {
            Empleado empleado = obtenerDatos();
            EmpleadoDAL.eliminar(empleado.getEmpleadoId());
            JOptionPane.showMessageDialog(this,
                    "El empleado fue eliminado exitosamente", "ELIMINAR EMPLEADO",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), "ERROR EMPLEADO",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

