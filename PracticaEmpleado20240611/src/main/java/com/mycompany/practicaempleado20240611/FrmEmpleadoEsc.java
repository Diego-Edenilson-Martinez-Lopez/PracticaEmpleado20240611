    private void asignarDatos(Empleado empleado) {
        jTextNombreEmpleado.setText(empleado.getNombre());
        jTextApelidoEmpleado.setText(empleado.getApellido());
        jTextCargo.setText(empleado.getCargo());
        jTextSalario.setText(String.valueOf(empleado.getSalario()));
    }

    private Empleado obtenerDatos() {
        Empleado empleado = new Empleado();
        empleado.setNombre(jTextNombreEmpleado.getText());
        empleado.setApellido(jTextApelidoEmpleado.getText());
        empleado.setCargo(jTextCargo.getText());
        empleado.setSalario(Double.parseDouble(jTextSalario.getText()));

        if (empleadoActual != null) {
            empleado.setEmpleadoId(empleadoActual.getEmpleadoId());
        }

        return empleado;
    }
