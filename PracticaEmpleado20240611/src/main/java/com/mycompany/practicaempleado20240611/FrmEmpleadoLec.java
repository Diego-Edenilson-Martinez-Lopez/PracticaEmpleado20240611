    private Empleado obtenerDatosEmpleado() {
        int row = jTableEmpleados.getSelectedRow();
        if (row != -1) {
            int empleadoId = (int) jTableEmpleados.getValueAt(row, 0);
            return EmpleadoDAL.obtenerPorId(empleadoId);
        }
        return null;
    }

   

  

   
