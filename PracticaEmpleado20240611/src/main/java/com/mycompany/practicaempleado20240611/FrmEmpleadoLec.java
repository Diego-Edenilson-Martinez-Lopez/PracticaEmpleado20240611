    private void jBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarActionPerformed
        // TODO add your handling code here:
        String nombreBuscar = jTextBuscar.getText();
        ArrayList<Empleado> empleadosEncontrados = new ArrayList<>();
        ArrayList<Empleado> empleados = EmpleadoDAL.obtenerTodos();
        for (Empleado empleado : empleados) {
            if (empleado.getNombre().contains(nombreBuscar)) {
                empleadosEncontrados.add(empleado);
            }
        }
        String[] columnas = {"EmpleadoID", "Nombre", "Apellido", "Cargo", "Salario"};
        Object[][] datos = new Object[empleadosEncontrados.size()][5];
        for (int i = 0; i < empleadosEncontrados.size(); i++) {
            Empleado empleado = empleadosEncontrados.get(i);
            datos[i][0] = empleado.getEmpleadoId();
            datos[i][1] = empleado.getNombre();
            datos[i][2] = empleado.getApellido();
            datos[i][3] = empleado.getCargo();
            datos[i][4] = empleado.getSalario();
        }
        DefaultTableModel modelTable = new DefaultTableModel(datos, columnas);
        jTableEmpleados.setModel(modelTable);
    }//GEN-LAST:event_jBtnBuscarActionPerformed

    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        // TODO add your handling code here:
        Empleado empleado = obtenerDatosEmpleado();
        if (empleado != null) {
            opcionCRUD = OpcionesCRUD.MODIFICAR;
            FrmEmpleadoEsc frmEmpleadoEsc = new FrmEmpleadoEsc(opcionCRUD, empleado);
            frmEmpleadoEsc.setTitle("Modificar Empleado");
            frmEmpleadoEsc.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Seleccionar una fila", "Empleado",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtnEditarActionPerformed

