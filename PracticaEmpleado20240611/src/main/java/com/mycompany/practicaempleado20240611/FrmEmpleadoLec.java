    private void cargarEmpleados() {
        ArrayList<Empleado> empleados = EmpleadoDAL.obtenerTodos();
        String[] columnas = {"EmpleadoID", "Nombre", "Apellido", "Cargo", "Salario"};
        Object[][] datos = new Object[empleados.size()][5];
        for (int i = 0; i < empleados.size(); i++) {
            Empleado empleado = empleados.get(i);
            datos[i][0] = empleado.getEmpleadoId();
            datos[i][1] = empleado.getNombre();
            datos[i][2] = empleado.getApellido();
            datos[i][3] = empleado.getCargo();
            datos[i][4] = empleado.getSalario();
        }
        DefaultTableModel modelTable = new DefaultTableModel(datos, columnas);
        jTableEmpleados.setModel(modelTable);
    }
